
// post decrement/increament
var num = 10;
var newnum = num++;
console.log(num);
console.log(newnum);

var num = 10;
var newnum = num--;
console.log(num);
console.log(newnum);
// pre decrement/increament
var num = 10;
var newnum = ++num;
console.log(num);
console.log(newnum);

var num = 10;
var newnum = --num;
console.log(num);
console.log(newnum);